# eksponen (pangkat)
a = 12
b = 23

hasil = a**b
print(a, "**", b, "=", hasil)
#  modulus
hasil = a % b
print(a, "%", b, "=", hasil)

# floor division
hasil = a // b
print(a, "//", b, "=", hasil)

# priotitas operasi
x = 3
y = 2
z = 9
hasil = x**y // x % z
print(x, "**", y, "//", x, "%", z, "=", hasil)
